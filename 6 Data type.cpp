#include <iostream>

int main() {
    // Declare and initialize a char variable
    char myChar = 'A';

    // Display the value of the char variable
    std::cout << "The value of myChar: " << myChar << std::endl;

    // Increment the ASCII value of the char variable
    myChar++;
    
    // Display the updated value of the char variable
    std::cout << "After incrementing, the value of myChar: " << myChar << std::endl;

    return 0;
}
